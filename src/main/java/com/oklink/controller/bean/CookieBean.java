package com.oklink.controller.bean;

public class CookieBean {
	private String coin_session_id;
	private String nikeName;
	private String loginInfo;
	private String userid;

	
	public String getCoin_session_id() {
		return coin_session_id;
	}
	public void setCoin_session_id(String coin_session_id) {
		this.coin_session_id = coin_session_id;
	}
	public String getNikeName() {
		return nikeName;
	}
	public void setNikeName(String nikeName) {
		this.nikeName = nikeName;
	}
	public String getLoginInfo() {
		return loginInfo;
	}
	public void setLoginInfo(String loginInfo) {
		this.loginInfo = loginInfo;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}

}
